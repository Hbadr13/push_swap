#include "libft.h"

int	ft_atoi(const	char	*str)
{
	char	*s;
	int		sing;
	int		i;
	int		nb;

	nb = 0;
	s = (char *)str;
	sing = 1;
	i = 0;
	while (s[i] == ' ' || s[i] == '\r' || s[i] == '\f'
		|| s[i] == '\v' || s[i] == '\n' || s[i] == '\t')
		i++;
	if (s[i] == '-' || s[i] == '+')
	{
		if (s[i] == '-')
			sing = sing * -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		nb = nb * 10 + s[i] - '0';
		i++;
	}
	return (nb * sing);
}
