#include"libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*p;
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	if (!s1 || !s2)
		return (NULL);
	p = malloc(sizeof(char ) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (p == NULL)
		return (0);
	while (s1[i])
	{
		p[i] = s1[i];
		i++;
	}
	while (s2[j])
	{
		p[i] = s2[j];
		i++;
		j++;
	}	
	p[i] = '\0';
	return (p);
}
